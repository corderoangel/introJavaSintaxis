package M_for.BuclesForComunes;
/*
* El bucle for tradicional se utiliza para iterar sobre una secuencia de valores conocida,
* como un rango numérico.
* */
public class for1 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Iteración " + i);
        }

    }
}
